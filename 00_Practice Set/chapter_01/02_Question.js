//Create two variables a = 10 and b = 20. Print their sum, difference, product, and quotient.

let a = 10;
let b = 20;

console.log("Sum:", a + b);        // 30
console.log("Difference:", a - b); // -10
console.log("Product:", a * b);    // 200
console.log("Quotient:", a / b);   // 0.5
console.log("Remainder:", a % b);  // 10